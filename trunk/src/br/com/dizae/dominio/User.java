package br.com.dizae.dominio;

public class User {
	
	private String facebookId_usuario;
	private String nome_usuario;
	private String login_usuario;
	
	public String getFacebookId_usuario() {
		return facebookId_usuario;
	}
	public void setFacebookId_usuario(String facebookId_usuario) {
		this.facebookId_usuario = facebookId_usuario;
	}
	public String getNome_usuario() {
		return nome_usuario;
	}
	public void setNome_usuario(String nome_usuario) {
		this.nome_usuario = nome_usuario;
	}
	public String getLogin_usuario() {
		return login_usuario;
	}
	public void setLogin_usuario(String login_usuario) {
		this.login_usuario = login_usuario;
	}
	

}
